package streams;

public class Produto {

    final String nome;
    final double preco;
    final int desconto;
    final String frete;

    public Produto(String nome, double preco, int desconto, String frete) {
        this.nome = nome;
        this.preco = preco;
        this.desconto = desconto;
        this.frete = frete;
    }
}

